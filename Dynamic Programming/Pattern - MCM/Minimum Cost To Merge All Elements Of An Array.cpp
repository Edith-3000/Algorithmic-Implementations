vvi dp;

int min_cost(vi &v) {
	int n = sz(v);
	if(n == 0) return 0;
	
	dp.clear();
	dp.resize(n, vi(n));
	
	for(int g = 0; g < n; g++) {
		for(int i = 0, j = g; j < n; i++, j++) {
			if(g == 0) dp[i][j] = 0;
			else {
				int sum = accumulate(v.begin() + i, v.begin() + j + 1, 0);
				int res = INT_MAX;
				for(int k = i; k <= (j - 1); k++) {
					int tmp = sum + dp[i][k] + dp[k+1][j];
					res = min(res, tmp);
				}
				
				dp[i][j] = res;
			}
		}
	}
	
	return dp[0][n-1];
}

