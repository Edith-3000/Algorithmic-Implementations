int min_cost(vi &v, int i, int j) {
	if(i > j) return 0;
	if(i == j) return 0;
	
	int sum = 0;
	for(int k = i; k <= j; k++) sum += v[k];
	
	int res = INT_MAX;
	for(int k = i; k <= (j - 1); k++) {
		int tmp = sum + min_cost(v, i, k) + min_cost(v, k + 1, j);
		res = min(res, tmp);
	}
	
	return res;
}

