int burst_balloon(vi &v) {
	int n = sz(v);
	vvi dp(n, vi(n));
	
	for(int g = 0; g < n; g++) {
		for(int i = 0, j = g; j < n; i++, j++) {
			int res = INT_MIN;
			
			for(int k = i; k <= j; k++) {
				int left = (k == i) ? 0 : dp[i][k-1];
				int right = (k == j) ? 0 : dp[k+1][j];
				int val = ((i == 0) ? 1 : v[i-1]) * v[k] * ((j == n - 1) ? 1 : v[j+1]);
				
				int tmp = val + left + right;
				res = max(res, tmp);
			}
			
			dp[i][j] = res;
		}
	}
	
	return dp[0][n-1];
}

