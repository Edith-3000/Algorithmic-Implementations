ll mod_exp(ll a, ll b) { a %= mod; if(a == 0) return 0LL; ll res = 1LL; 
                         while(b > 0) { if(b & 1) res = (res * a) % mod; a = (a * a) % mod; b >>= 1; } return res; }
                         
ll mod_inv(ll a) { return mod_exp(a, mod - 2); } // works only for prime value of "mod"
ll GCD(ll a, ll b) { return (b == 0) ? a : GCD(b, a % b); }

/******************************************************************************************************************************/

int helper(int i, int j, vi &v) {
	// base condition
	if(i > j) return 0;
	
	// to store the final result
	int res = INT_MIN;
	
	// "k loop scheme"
	// this scheme tells that if in the subarray v[i....j] if v[k] is the 
	// last balloon to be bursted then what maximum profit we can obtain from v[i....j]
	for(int k = i; k <= j; k++) {
		int left = helper(i, k - 1, v);
		int right = helper(k + 1, j, v);
		int val = v[i - 1] * v[k] * v[j + 1];
		
		int tmp_ans = val + left + right;
		res = max(res, tmp_ans);
	}
	
	return res;
}

int burst_balloon(vi &v) {
	int n = sz(v);
	if(n == 0) return 0;
	
	v.insert(v.begin(), 1);
	v.pb(1);
	
	return helper(1, n, v);
}

