// NOTE: the function returns 0-based indices
pii kadane_algo(vi &v) {
	int n = sz(v);
	if(n == 0) return {-1, -1};
	
	// to store the maximum sum subarray
	ll mx = v[0];
	
	// to store end of maximum sum subarray found so far
	int end = 0;
		
	// stores the maximum sum of subarray ending at the current position i
	ll max_ending_here = v[0];
	
	for(int i = 1; i < n; i++) {
		// update the maximum sum of subarray "ending" at index 'i' (by adding the
        // current element to maximum sum ending at previous index 'i-1')
		max_ending_here += v[i];
		
		// maximum sum should be more than the current element
		max_ending_here = max(max_ending_here, (ll)v[i]);
				
		// update the result if the current subarray sum is found to be greater
		if(mx < max_ending_here) {
			end = i;
			mx = max_ending_here;
		}
	}
	
	int start = end;
	
	while(start >= 0) {
		mx -= v[start];
		if(mx == 0) break;
		start -= 1;
	}
	
	return {start, end};
}

void solve()
{
  	int n; cin >> n;
  	vi v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	pii res = kadane_algo(v);
  	cout << res.F << " " << res.S << "\n";
}

