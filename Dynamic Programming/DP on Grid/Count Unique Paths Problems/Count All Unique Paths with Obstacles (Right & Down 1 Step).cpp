// Function to compute the total #unique grid paths from cell (i, j) to cell (n - 1, m - 1),
// if only right or downward movement is allowed with obstacles present
int grid_unique_paths_with_obstacles(int i, int j, int n, int m, vvi &v) {
    // base case(s)
    if(i >= n or j >= m) return 0;
    if(v[i][j] == 1) return 0;
    if(i == (n - 1) and j == (m - 1)) return 1;
    
    int rg = grid_unique_paths_with_obstacles(i, j + 1, n, m, v);
    int dn = grid_unique_paths_with_obstacles(i + 1, j, n, m, v);
    
    return (rg + dn);
}

void solve()
{
    int n, m; cin >> n >> m;
    vvi v(n, vi(m));

    for(int i = 0; i < n; i++) {
        for(int j = 0; j < m; j++) cin >> v[i][j];
    }

    cout << grid_unique_paths_with_obstacles(0, 0, n, m, v) << "\n";
}

