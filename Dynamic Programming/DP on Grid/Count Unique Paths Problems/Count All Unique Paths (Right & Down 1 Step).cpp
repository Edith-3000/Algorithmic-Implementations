// Function to compute the total #unique grid paths from cell (i, j) to cell (n - 1, m - 1),
// if only right or downward movement is allowed
ll grid_unique_paths(int n, int m) {
    int N = n + m - 2;
    int r = m - 1; // we could also use (n - 1) here
    
    ll res = 1LL;
    
    for(int i = 1; i <= r; i++) {
        res *= (N - r + i);
        res /= i;
    }
    
    return res;
}

