int count_squares(vvi &v) {
	int n = sz(v);
	if(n == 0) return 0;
	
	int m = sz(v[0]);
	
	int res = 0;
	
	// dp[i][j] = #square submatrices with all ones such that their bottom right cell is (i, j)
	vvi dp(n, vi(m));
	
	for(int i = 0; i < n; i++) {
		dp[i][0] = v[i][0];
		res += dp[i][0];
	}
	
	for(int j = 1; j < m; j++) {
		dp[0][j] = v[0][j];
		res += dp[0][j];
	}
		
	for(int i = 1; i < n; i++) {
		for(int j = 1; j < m; j++) {
			if(v[i][j] == 0) dp[i][j] = 0;
			else dp[i][j] = min({dp[i - 1][j], dp[i - 1][j - 1], dp[i][j - 1]}) + v[i][j];
			res += dp[i][j];
		}
	}
	
	return res;
}

void solve()
{
  	int n, m; cin >> n >> m;
  	
  	vvi v(n, vi(m));
  	
  	for(int i = 0; i < n; i++) {
  		for(int j = 0; j < m; j++) {
  			cin >> v[i][j];
  		}
  	}
  	
  	cout << count_squares(v) << "\n";
}

