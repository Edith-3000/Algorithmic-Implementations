// dp[i][j][l] = #paths from (i, j) to (n - 1, m - 1) such that the sum of elements in the path % k = l
vector<vector<vector<ll>>> dp;

ll cnt_paths(ll i, ll j, ll mod_val, ll k, ll n, ll m, vvll &v) {
	// base case(s)
	if((i == n - 1) and (j == m - 1)) {
		return ((v[i][j] % k) == mod_val);
	}
	
	// check if already calculated
	if(dp[i][j][mod_val] != -1) {
            return dp[i][j][mod_val];
        }
	
	ll ways_down = 0LL, ways_right = 0LL;
	
	ll new_mod_val = (mod_val - (v[i][j] % k) + k) % k;
	
	if((i + 1) < n) {
		ways_down = cnt_paths(i + 1, j, new_mod_val, k, n, m, v);
	}
	
	if((j + 1) < m) {
		ways_right = cnt_paths(i, j + 1, new_mod_val, k, n, m, v);
	}
	
	ll res = (ways_down + ways_right) % mod;
	
	return dp[i][j][mod_val] = res;
}

void solve()
{
  	ll n, m; cin >> n >> m;
  	
  	vvll v(n, vll(m));
  	
  	for(ll i = 0; i < n; i++) {
  		for(ll j = 0; j < m; j++) cin >> v[i][j];
  	}
  	
  	ll k; cin >> k;
  	
  	dp.clear();
    dp.resize(n, vvll(m, vll(k, -1)));
  	
  	cout << cnt_paths(0, 0, 0, k, n, m, v);
}

