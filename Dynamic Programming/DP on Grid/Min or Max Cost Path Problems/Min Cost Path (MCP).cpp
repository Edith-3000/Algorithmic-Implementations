// Function to compute minimum cost path from cell (0, 0) to cell (n - 1, m - 1),
// if only right or downward movement is allowed
int mcp(vvi &v) {
    int n = sz(v);
    if(n == 0) return 0;
    
    int m = sz(v[0]);
    
    vvi dp(n, vi(m));
    
    for(int i = n - 1; i >= 0; i--) {
        dp[i][m - 1] = v[i][m - 1];
        if(i != (n - 1)) dp[i][m - 1] += dp[i + 1][m - 1];
    }
    
    for(int j = m - 2; j >= 0; j--) {
        dp[n - 1][j] = v[n - 1][j] + dp[n - 1][j + 1];
    }
    
    for(int i = n - 2; i >= 0; i--) {
        for(int j = m - 2; j >= 0; j--) {
            dp[i][j] = v[i][j] + min(dp[i + 1][j], dp[i][j + 1]);
        }
    }
    
    return dp[0][0];
}

