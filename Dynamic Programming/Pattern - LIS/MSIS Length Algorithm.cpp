// dp[i] = MSIS ending at index i of the vector v[]
vll dp;

ll msis(vi &v, int n) {
	dp.clear();
	dp.resize(n, 0);
	
	// initialisation of dp matrix 
	// MSIS ending with v[0] has sum = v[0]
	dp[0] = v[0];
	
	for(int i = 1; i < n; i++) {
		for(int j = 0; j < i; j++) {
			if(v[j] < v[i] and dp[j] > dp[i]) dp[i] = dp[j];
		}
		
		dp[i] += v[i];
	}
	
	ll res = dp[0];
	for(int i = 1; i < n; i++) res = max(res, dp[i]);
	
	return res;
}

