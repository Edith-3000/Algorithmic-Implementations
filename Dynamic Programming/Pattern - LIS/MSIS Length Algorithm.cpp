int msis(vi &v, int n, int prev, int sum) {
	// base case
	if(n == 0) return sum;
	
	// choice diagram code
	// for each element of v[], we have 2 choices ==>
	// Choice 1: exclude the current element and process the remaining elements
	// Choice 2: include the current element if it is smaller than prev element 
	
	int exclude = msis(v, n - 1, prev, sum);
	
	int include = sum;
	if(v[n-1] < prev) include = msis(v, n - 1, v[n-1], sum + v[n-1]);
	
	// return maximum of above two choices
	return max(exclude, include);
}

