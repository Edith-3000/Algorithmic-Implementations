void solve()
{
  	int n; cin >> n;
  	vll v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	vvll dp(n, vll(n, 0LL));
  	vvll sum(n, vll(n, 0LL));
  	
  	for(int g = 0; g < n; g++) {
  		for(int i = 0, j = g; j < n; i++, j++) {
  			if(g == 0) sum[i][j] = v[i];
  			else if(g == 1) sum[i][j] = v[i] + v[j];
  			else sum[i][j] = sum[i][j-1] + sum[i+1][j] - sum[i+1][j-1];
  		}
  	}
  	
  	for(int g = 0; g < n; g++) {
  		for(int i = 0, j = g; j < n; i++, j++) {
  			if(g == 0) dp[i][j] = 0LL;
  			else {
  				dp[i][j] = INF;
  				
  				for(int k = i; k <= (j - 1); k++) {
  					dp[i][j] = min(dp[i][j], dp[i][k] + dp[k+1][j]);
  				}
  				
  				dp[i][j] += sum[i][j];
  			}
  		}
  	}
  	
  	cout << dp[0][n-1] << "\n";
}

