// dp[i][j] = the probability of getting at least j #heads from the first i coins
double dp[3000][1501];
vector<double> v;
int n;

double calc(int i, int j) {
	// base case(s)
	if(j == 0) return 1.0;
	if(i == 0) return 0.0;
	
	// check if already calculated or not
	// NOTE: Avoid using "==" or "!=" for comparing doubles due to precision errors
	if(dp[i][j] > -0.9) return dp[i][j];
	
	double op1 = v[i] * calc(i - 1, j - 1);
	double op2 = (1.0 - v[i]) * calc(i - 1, j);
	
	return dp[i][j] = op1 + op2;
}

void solve()
{
  	cin >> n;
  	
  	vset(v, n + 1, 0.0);
  	memset(dp, -1.0, sizeof dp);
  	
  	for(int i = 1; i <= n; i++) cin >> v[i];
  	
  	double res = calc(n, (n + 1) / 2);
  	cout << fixed << setprecision(9) << res << "\n";
}

