ll zero_one_knapsack(vi &wt, vi &val, ll w, int n) {
    // space optimized dp[] vector
    vll dp(w + 1);
    
    for(int i = 0; i <= w; i++) dp[i] = 0LL;
    
    for(int i = 0; i < n; i++) {
        for(int j = w; j >= 0; j--) {
            ll exc = dp[j];
            ll inc = LLONG_MIN;
            if(wt[i] <= j) inc = val[i] + dp[j - wt[i]];
            dp[j] = max(exc, inc);
        }
    }
    
    return dp[w];
}

void solve()
{
    int n; ll w; cin >> n >> w;

    vi wt(n), val(n);

    for(int i = 0; i < n; i++) cin >> wt[i];
    for(int i = 0; i < n; i++) cin >> val[i];
    
    cout << zero_one_knapsack(wt, val, w, n) << "\n"; 
}

