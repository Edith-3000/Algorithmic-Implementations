int max_profit(vi &v) {
	int n = sz(v);
	if(n == 0) return 0;
	
	vvi dp(n + 1, vi(2));
	
	dp[n][0] = dp[n][1] = 0;
	
	for(int i = n - 1; i >= 0; i--) {
		for(int j = 0; j <= 1; j++) {
			if(j == 1) {
				int bought = -v[i] + dp[i + 1][0];
				int notBought = dp[i + 1][1];
				
				dp[i][j] = max(bought, notBought);
			}
			
			else {
				int sold = v[i] + ((i + 2 <= n) ? dp[i + 2][1] : 0);
				int notSold = dp[i + 1][0];
				
				dp[i][j] = max(sold, notSold);
			}
		}
	}
	
	int res = dp[0][1];
	
	return res;
}

