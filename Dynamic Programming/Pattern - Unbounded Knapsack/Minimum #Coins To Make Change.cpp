int min_coins(vi &v, int n, int sum) {
	// base case(s)
	if(sum == 0) return 0;
	
	// inttialize result
	int res = INT_MAX;
	
	for(int i = 0; i < n; i++) {
		if(v[i] > sum) continue;
		int tmp = min_coins(v, n, sum - v[i]);
		if(tmp != INT_MAX and tmp + 1 < res) res = tmp + 1;
	}
	
	return res;
}

