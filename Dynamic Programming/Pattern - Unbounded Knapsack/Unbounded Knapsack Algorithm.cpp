ll ub_knapsack(vi &wt, vi &val, ll w, int n) {
	// base condition(s)
	if(n == 0 || w == 0) return 0LL;
	
	// choice diagram code
	if(wt[n-1] <= w) return max(val[n-1] + ub_knapsack(wt, val, w - wt[n-1], n),
	                            ub_knapsack(wt, val, w, n - 1));
	
	// when wt[n-1] > w                        
	else return ub_knapsack(wt, val, w, n - 1);
}

