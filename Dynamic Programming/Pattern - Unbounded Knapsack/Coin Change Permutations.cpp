vi dp;

int cnt_ways(vi &v, int n, int sum) {
	// initialisation of dp matrix
	for(int i = 0; i <= sum; i++) dp[i] = 0;
	
	// there is only 1 ways to form sum = 0 
	dp[0] = 1;

	// choice diagram code iterative version
	for(int amt = 1; amt <= sum; amt++) {
		for(int i = 0; i < n; i++) {
			if(v[i] > amt) continue;
			dp[amt] += dp[amt - v[i]];
		}
	}

	return dp[sum];
}

