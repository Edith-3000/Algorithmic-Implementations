vvi dp;

int cut_rod(vi &prices,int len) {
	// initialisation of dp matrix
	for(int i = 0; i <= len; i++) dp[i][0] = 0;
	for(int j = 1; j <= len; j++) dp[0][j] = 0;
	
	// choice diagram code iterative version
	for(int i = 1; i <= len; i++) {
		for(int j = 1; j <= len; j++) {
			if(i <= j) dp[i][j] = max(prices[i-1] + dp[i][j-i], dp[i-1][j]);
			else dp[i][j] = dp[i-1][j];
		}
	}
	
	return dp[len][len];
}

void solve()
{
  	int len; cin >> len;
  	vi prices(len);
  	for(int i = 0; i < len; i++) cin >> prices[i];
  	
  	dp.resize(len + 1);
  	for(int i = 0; i <= len; i++) dp[i].resize(len + 1);
  	
  	cout << cut_rod(prices, len) << "\n";
}

