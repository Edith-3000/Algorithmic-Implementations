vvi dp;

// NOTE: here handling of 0 is not required, as a coin with 0
//       denomination is meaningless
int cnt_ways(vi &v, int n, int sum) {
	// initialisation of dp matrix
	for(int i = 0; i <= n; i++) dp[i][0] = 1;
	for(int j = 1; j <= sum; j++) dp[0][j] = 0;

	// choice diagram code iterative version
	for(int i = 1; i <= n; i++) {
	   for(int j = 1; j <= sum; j++) {
	       if(v[i-1] <= j) dp[i][j] = dp[i][j - v[i-1]] + dp[i-1][j];
	       else dp[i][j] = dp[i-1][j];
	   }
	}

	return dp[n][sum];
}

void solve()
{
  	int n, sum; cin >> n >> sum;
  	vi v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	dp.resize(n + 1);
  	for(int i = 0; i <= n; i++) dp[i].resize(sum + 1);
  	
  	cout << cnt_ways(v, n, sum) << "\n"; 
}

