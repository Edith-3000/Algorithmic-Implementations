int cnt_ways(vi &v, int n, int sum) {
	// base case(s)
	if(sum == 0) return 1;
	if(sum < 0) return 0;
	if(n <= 0) return 0;
	
	return cnt_ways(v, n, sum - v[n-1]) + cnt_ways(v, n - 1, sum);
}

