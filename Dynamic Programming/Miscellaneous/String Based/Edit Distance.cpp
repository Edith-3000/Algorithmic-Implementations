int edit_distance(string &s1, string &s2, int n, int m) {
	// base case(s)
	
	// if first string is empty, the only option is to insert all 
	// characters of second string into first
	if(n == 0) return m;
	
	// if second string is empty, the only option is to remove all 
	// characters of first string
	if(m == 0) return n;
	
	// case 1: when last characters match
	if(s1[n-1] == s2[m-1]) return edit_distance(s1, s2, n - 1, m - 1);
	
	// case 2: when last characters do not match
	int add = 1 + edit_distance(s1, s2, n, m - 1);
	int remove = 1 + edit_distance(s1, s2, n - 1, m);
	int replace = 1 + edit_distance(s1, s2, n - 1, m - 1);
	
	return min(add, min(remove, replace));
}

