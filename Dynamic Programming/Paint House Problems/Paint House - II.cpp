int find_ans(vvi &v) {
	int n = sz(v);
	if(n == 0) return 0;
	int m = sz(v[0]);
	
	vvi dp(n, vi(m));
	
	// mn = minimum, smn = second minimum
	int mn = INT_MAX, smn = INT_MAX;
	
	for(int j = 0; j < m; j++) {
		dp[0][j] = v[0][j];
		if(v[0][j] <= mn) smn = mn, mn = v[0][j];
		else if(v[0][j] <= smn) smn = v[0][j];
	}
	
	for(int i = 1; i < n; i++) {
		int nmn = INT_MAX, nsmn = INT_MAX;
		for(int j = 0; j < m; j++) {
			if(v[i][j] == mn) dp[i][j] = v[i][j] + smn;
			else dp[i][j] = v[i][j] + mn;
			
			if(dp[i][j] <= nmn) nsmn = nmn, nmn = dp[i][j];
			else if(dp[i][j] <= nsmn) nsmn = dp[i][j];
		}
		
		mn = nmn;
		smn = nsmn;
	}
	
	return mn;
}

void solve()
{
  	int n, m; cin >> n >> m;
  	vvi v(n, vi(m));
  	for(int i = 0; i < n; i++) {
  		for(int j = 0; j < m; j++) cin >> v[i][j];
  	}
  	
  	cout << find_ans(v) << "\n";
}

