int len_SCS(string &s1, string &s2, int n, int m) {
    // base case(s)
    if(n == 0) return m;
    if(m == 0) return n;
    
    // choice diagram code
    
    // if last characters of both strings matches
    if(s1[n - 1] == s2[m - 1]) return 1 + len_SCS(s1, s2, n - 1, m - 1);
    
    // if last characters of both strings do not matches
    else return 1 + min(len_SCS(s1, s2, n, m - 1), 
                        len_SCS(s1, s2, n - 1, m));
}

void solve()
{
    string s1, s2; cin >> s1 >> s2;
    
    int n = sz(s1);
    int m = sz(s2);
    
    cout << len_SCS(s1, s2, n, m) << "\n";
}

