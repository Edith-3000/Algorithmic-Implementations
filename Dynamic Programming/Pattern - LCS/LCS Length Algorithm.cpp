int len_LCS(string &s1, string &s2, int n, int m) {
    vi prv(m + 1), cur(m + 1);
    
    for(int j = 0; j <= m; j++) {
        prv[j] = 0;
    }
    
    for(int i = 1; i <= n; i++) {
        for(int j = 0; j <= m; j++) {
            if(j == 0) cur[j] = prv[j];
            else {
                if(s1[i - 1] == s2[j - 1]) cur[j] = 1 + prv[j - 1];
                else cur[j] = max(cur[j - 1], prv[j]);
            }
        }
        
        prv = cur;
    }
    
    return prv[m];
}

