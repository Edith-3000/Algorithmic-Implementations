int len_LCS(string &s1, string &s2, int n, int m) {
	// base case(s)
	if(n == 0 or m == 0) return 0;
	
	// choice diagram code
	// if last characters of both strings matches
	if(s1[n-1] == s2[m-1]) return 1 + len_LCS(s1, s2, n - 1, m - 1);
	
	// if last characters of both strings do not matches
	else return max(len_LCS(s1, s2, n, m - 1), 
	                len_LCS(s1, s2, n - 1, m));
}

