mt19937_64 rang(chrono::high_resolution_clock::now().time_since_epoch().count());
int rng(int lim) {
    uniform_int_distribution<int> uid(0,lim-1);
    return uid(rang);
}

const int INF = 0x3f3f3f3f;
const ll mod = 1e9+7;

ll cnt_triangles(vi &v) {
	ll res = 0LL;
	int n = (int)v.size();
	if(n == 0) return res;
	
	sort(v.begin(), v.end());
	
	for(int i = 0; i + 2 < n; i++) {		
		for(int j = i + 1; j + 1 < n; j++) {
			int x = v[i] + v[j];
			int pos = lower_bound(v.begin() + j + 1, v.end(), x) - v.begin();
			res = (res + (pos - 1 - j)) % mod;
		}
	}
	
	return res;
}

void solve()
{
  	int n; cin >> n;
  	vi v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	cout << cnt_triangles(v) << "\n";
}

