vll count_smaller(vll &v) {
	ll n = sz(v);
	
	// res[i] = count of elements from [i + 1, n) which are strictly less than v[i]
	vll res(n, 0LL);
	
	pbds st;
        
    for(ll i = n - 1; i >= 0; i--) {
        auto it = st.lower_bound({v[i], -1});
        if(it == st.end()) res[i] = st.size();
        else {
            pll p = *it;
            res[i] = st.order_of_key(p);
        }
            
        st.insert({v[i], i});
    }
        
    return res;
}

void solve()
{
  	ll n; cin >> n;
  	vll v(n);
  	for(ll i = 0; i < n; i++) cin >> v[i];
  	
  	vll res = count_smaller(v);
  	
  	for(auto x: res) {
  		cout << x << " ";
  	} 
  	
  	cout << "\n";
}

