void merge_procedure(vpll &w, ll start, ll end, ll n, vll &res) {
	// temporary vector to store the sorted result 
	vpll tmp(end - start + 1);
	ll mid = start + (end - start)/2;
	
	// i = iterator for the first half
	// j = iterator for the second half
	// k = iterator fot the tmp[] vector
	ll i = start, j = mid + 1, k = 0;
	
	while(i <= mid && j <= end) {
		if(w[i].F <= w[j].F) tmp[k++] = w[j++];
		else {
			res[w[i].S] += (end - j + 1);
			tmp[k++] = w[i++];
		}
	}
	
	// fill the array if some of the elements are still left
	while(i <= mid) tmp[k++] = w[i++];
	while(j <= end)	tmp[k++] = w[j++];
	
	// copying back the sorted elements of the 2 parts
	// in the original array
	for(ll x = start; x <= end; x++) w[x] = tmp[x - start];
}

// Sorts the vector w in DESCENDING ORDER
void merge_sort(vpll &w, ll start, ll end, ll n, vll &res) {
	// Base case(s)
	if(start >= end) return;
	
	// Step 1: divide the array
	int mid = start + (end - start)/2;
	
	// Step 2: sort the array (recursively)
	merge_sort(w, start, mid, n, res);
	merge_sort(w, mid + 1, end, n, res);
	
	// Step 3: merge the sorted array back
	merge_procedure(w, start, end, n, res);
}

vll count_smaller(vll &v) {
	ll n = sz(v);
	
	// res[i] = count of elements from [i + 1, n) which are strictly less than v[i]
	vll res(n, 0LL);
	
	vpll w(n);
	
	for(ll i = 0; i < n; i++) {
		w[i].F = v[i];
		w[i].S = i;
	}
    
    merge_sort(w, 0, n - 1, n, res);
    
    return res;
}

