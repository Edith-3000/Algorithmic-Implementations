ListNode* swap_nodes(ListNode* head, int k) {
	if(!head or !head->next) return head;
	
	ListNode* p1 = head;
	ListNode* p1Prev = NULL;
	
	for(int i = 1; i < k; i++) {
		p1Prev = p1;
		p1 = p1->next;
	}
	
	ListNode *p2 = head, *itr = p1->next;
	ListNode *p2Prev = NULL;
	
	while(itr != NULL) {
		p2Prev = p2;
		p2 = p2->next;
		itr = itr->next;
	}
	
	// case when k = 1
	if(p1Prev == NULL) {
		p2Prev->next = p1;
		head = p2;
	}
	
	// case when k = length of LL
	else if(p2Prev == NULL) {
		p1Prev->next = p2;
		head = p1;
	}
	
	// otherwise
	else {
		p1Prev->next = p2;
		p2Prev->next = p1;
	}
	
	swap(p1->next, p2->next);
	
	return head;
}

