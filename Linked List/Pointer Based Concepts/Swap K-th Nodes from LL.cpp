// Making a node class containing the value and a pointer
// to next available node
class ListNode {
	public:
		int val;
		ListNode *next;
		ListNode() : val(0), next(nullptr) {}
        ListNode(int x) : val(x), next(nullptr) {}
        ListNode(int x, ListNode *next) : val(x), next(next) {}
};

class LinkedList {
	public:
		// head and tail pointers
		ListNode *head, *tail;
		
		// default constructor. Initializing head and tail pointers
		LinkedList() {
			head = NULL;
			tail = NULL;
		}
		
		// inserting elements (at the end of the list)
		void insert(int data) {
			// make a new node
			ListNode *new_node = new ListNode;
			new_node->val = data;
			new_node->next = NULL;
			
			// If list is empty, make the new node, the head
			// initialise tail also as new node
			if(head == NULL) {
				head = new_node;
				tail = new_node;
			}
			
			else {
				tail->next = new_node;
				tail = tail->next;
			}
		}
		
		void display() {
			ListNode *tmp = head;
			while(tmp != NULL) {
				cout << tmp->val;
				tmp = tmp->next;
				if(tmp != NULL) cout << "->";
			}
			
			cout << "\n";
		}		
};

ListNode* swap_nodes(ListNode* head, int k) {
	if(!head or !head->next) return head;
	
	ListNode* p1 = head;
	
	for(int i = 1; i < k; i++) {
		p1 = p1->next;
	}
	
	ListNode *p2 = head, *itr = p1->next;
	
	while(itr != NULL) {
		p2 = p2->next;
		itr = itr->next;
	}
	
	swap(p1->val, p2->val);
	
	return head;
}

void solve()
{
  	int n; cin >> n;
  	LinkedList l;
  	
  	for(int i = 0; i < n; i++) {
  		int x; cin >> x;
  		l.insert(x);
  	}
  	
  	// k is 1-based indexed
  	int k; cin >> k;
  	  	
  	l.display();
  	l.head = swap_nodes(l.head, k);
  	l.display();
  	
	cout << "\n";
}

