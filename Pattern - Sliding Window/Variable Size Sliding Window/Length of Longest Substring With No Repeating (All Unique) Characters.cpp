bool valid_window(vi &cnt) {
	bool ok = 1;
        
    for(int i = 0; i < 256; i++) {
        ok &= (cnt[i] <= 1);
    }
        
    return ok;
}

int LLSU(string &s) {
	int n = (int)s.size();
	if(n == 0) return 0;
	
	int res = 0, i = 0, j = 0;
	vi cnt(256, 0);
	
	while(j < n) {
		cnt[s[j]] += 1;
		
		if(valid_window(cnt)) {
            res = max(res, j - i + 1);
            j++;
        }
		
		else {
			while(!valid_window(cnt)) {
				cnt[s[i]] -= 1;
				i++;
			}
			
			j++;
		}
	}
	
	return res;
}

