int LLSU(string &s) {
	int n = (int)s.size();
	if(n == 0) return 0;
	
	int res = 0, i = 0, j = 0;
	map<char, int> cnt;
	
	while(j < n) {
		cnt[s[j]] += 1;
		
		if((int)cnt.size() == (j - i + 1)) {
			res = max(res, j - i + 1);
			j++;
		}
		
		else {
			while((int)cnt.size() < (j - i + 1)) {
				cnt[s[i]] -= 1;
				if(cnt[s[i]] == 0) cnt.erase(s[i]);
				i++;
			}
			
			j++;
		}
	}
	
	return res;
}

