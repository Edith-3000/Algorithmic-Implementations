// Function to return the 0-based indices of the max element in all subarrays of size k, if
// there are more than 1 elements with max value in a k sized subarray, then it returns 
// the element with the lowest index
vi max_of_all_subarr(vi &v, int k) {
	int n = (int)v.size();
	vi indices;
	
	if(k > n) {
		indices.pb(max_element(v.begin(), v.end()) - v.begin());
		return indices;
	}
	
	if(n == 0) return indices;
	
	int i = 0, j = 0;
	deque<int> dq;
	
	while(j < n) {
		// removing all those elements which are to the left and also less 
		// than current element (v[j]), since these elements can't be candidates
		// for future answer 
		while(!dq.empty() and v[dq.back()] < v[j]) dq.pop_back();
		
		// taking into consideration the current element
		dq.push_back(j);
		
		// if window size not reached increment j
		if((j - i + 1) < k) j++;
		
		// if window size has been reached
		else {
			// the maximum element for the current window will always be at front
			indices.pb(dq.front());
			
			// removing the calculation done for the ith element i.e. v[i]
			if(dq.front() == i) dq.pop_front();
			
			// sliding the window
			i++; j++;
		}
	}
		
	return indices; 
}

