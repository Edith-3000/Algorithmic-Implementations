vi max_of_all_subarr(vi &v, int k) {
	int n = (int)v.size();
	vi res;
	
	if(k > n) {
		res.pb(*max_element(v.begin(), v.end()));
		return res;
	}
	
	if(n == 0) return res;
	
	int i = 0, j = 0;
	map<int, int> cnt;
	
	while(j < n) {		
		// taking into consideration the current element
		cnt[v[j]] += 1;
		
		// if window size not reached increment j
		if((j - i + 1) < k) j++;
		
		// if window size has been reached
		else {
			// the maximum element for the current window will always be the last key of map
			res.pb((prev(cnt.end(), 1))->first);
			
			// removing the calculation done for the ith element i.e. v[i]
			cnt[v[i]] -= 1;
            if(cnt[v[i]] == 0) cnt.erase(v[i]);
			
			// sliding the window
			i++; j++;
		}
	}
	
	return res; 
}

