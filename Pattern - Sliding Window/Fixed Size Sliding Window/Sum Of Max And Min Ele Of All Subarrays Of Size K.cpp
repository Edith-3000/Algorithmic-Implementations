ll sum_max_min(vi &v, int k) {
	int n = sz(v);
	if(n == 0 or k == 0) return 0LL;
	
	if(k > n) {
		ll mx = *max_element(v.begin(), v.end());
		ll mn = *min_element(v.begin(), v.end());
		return mx + mn;
	}
	
	ll res = 0LL;
	
	int i = 0, j = 0;
	deque<int> G, S;
	
	while(j < n) {
		// removing all those elements which are to the left and also less 
		// than current element (v[j]), since these elements can't be candidates
		// for future answer 
		while(!G.empty() and G.back() < v[j]) G.pop_back();
		
		// removing all those elements which are to the left and also greater
		// than current element (v[j]), since these elements can't be candidates
		// for future answer 
		while(!S.empty() and S.back() > v[j]) S.pop_back();
		
		// taking into consideration the current element for min and max
		G.push_back(v[j]);
		S.push_back(v[j]);
		
		// if window size not reached increment j
		if((j - i + 1) < k) j++;
		
		// if window size has been reached
		else {
			// the max and min element for the current window will always be at front
			res += ((ll)G.front() + S.front());
			
			// removing the calculation done for the ith element i.e. v[i]
			if(G.front() == v[i]) G.pop_front();
			if(S.front() == v[i]) S.pop_front();
			
			// sliding the window
			i++; j++;
		}
	}
	
	return res; 
}

