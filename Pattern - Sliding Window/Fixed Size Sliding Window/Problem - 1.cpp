ll cnt_hugs(ll x) {
	return (x * (x + 1)) / 2LL;
}

// https://codeforces.com/blog/entry/77869?#comment-630247
void solve()
{
  	ll n, d; cin >> n >> d;
  	vll v(2 * n);
  	
  	for(ll i = 0; i < n; i++) {
  		cin >> v[i];
  		v[i + n] = v[i];
  	}
  	
  	ll res = 0LL;
  	
  	ll days = 0LL, hugs = 0LL;
  	ll i = 0, j = 0;
  	
  	while(j < (2 * n)) {
  		days += v[j];
  		hugs += cnt_hugs(v[j]);
  		
  		while(days > d) {
  			if(days - v[i] < d) break;
  			days -= v[i];
  			hugs -= cnt_hugs(v[i]);
  			i += 1;
  		}
  		
  		ll diff = max(days - d, 0LL);
  		res = max(res, hugs - cnt_hugs(diff));
  		
  		j += 1;
  	}
  	
  	cout << res << "\n";
}

