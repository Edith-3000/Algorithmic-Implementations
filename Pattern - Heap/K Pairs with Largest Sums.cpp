/******************************************************************************************************************************/

struct node {
	int val, idxa, idxb;
};

struct cmp {
	bool operator() (const node &n1, const node &n2) {
		return n1.val < n2.val;
	}
};

// NOTE: for the case in which (n * m) < k, it returns all the possible (n * m) pairs
vpii k_largest_pairs(vi &a, vi &b, int k) {
	int n = sz(a), m = sz(b);
	
	sort(a.begin(), a.end());
	sort(b.begin(), b.end());
	
	priority_queue<node, vector<node>, cmp> mnh;
	mnh.push({a[n - 1] + b[m - 1], n - 1, m - 1});
	
	set<pii> taken;
	taken.insert({n - 1, m - 1});
	
	vpii res;
	
	while(sz(res) != k) {
		node cur = mnh.top();
		mnh.pop();
		
		int i = cur.idxa, j = cur.idxb;
		
		res.pb({a[i], b[j]});
		
		// case in which (n * m) < k
		if((i == 0) and (j == 0)) break;
		
		if((i > 0) and (taken.count({i - 1, j}) == 0)) {
			mnh.push({a[i - 1] + b[j], i - 1, j});
			taken.insert({i - 1, j});
		}
		
		if((j > 0) and (taken.count({i, j - 1}) == 0)) {
			mnh.push({a[i] + b[j - 1], i, j - 1});
			taken.insert({i, j - 1});
		}
	}
	
	return res;
}

void solve()
{
  	int n, m, k; 
  	cin >> n >> m >> k;
  	
  	vi a(n);
  	for(int i = 0; i < n; i++) cin >> a[i];
  	
  	vi b(m);
  	for(int i = 0; i < m; i++) cin >> b[i];
  	
  	vpii res = k_largest_pairs(a, b, k);
  	
  	for(auto x: res) {
  		cout << x.F << " " << x.S << "\n";
  	}
}

