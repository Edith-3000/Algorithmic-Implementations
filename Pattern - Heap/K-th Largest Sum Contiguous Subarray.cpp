ll kth_largest_subarr(vi &v, int k) {
	int n = sz(v);
	if(n == 0) return 0LL;
	
	// array to store prefix sums
    vll cs(n + 1);
    cs[0] = 0LL;
    cs[1] = v[0];
    
    for (int i = 2; i <= n; i++) {
    	cs[i] = cs[i-1] + v[i-1];
    }
 	
    // priority_queue of min heap
    priority_queue<ll, vll, greater<ll>> q;
 
    // loop to calculate the contiguous subarray sum position-wise
    for (int i = 1; i <= n; i++) {
        // loop to traverse all positions that form contiguous subarray
        for (int j = i; j <= n; j++) {
            // calculates the contiguous subarray sum from i to j index
            ll x = cs[j] - cs[i-1];
            
            q.push(x);
            if(sz(q) > k) q.pop();
        }
    }
 
    // the top element will be then kth largest element
    return q.top();
}

void solve()
{
  	int n, k; cin >> n >> k;
  	vi v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	cout << kth_largest_subarr(v, k) << "\n";
}

