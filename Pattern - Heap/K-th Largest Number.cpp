int kth_largest(vi &v, int k) {
    int n = sz(v);
    if(k > n) return INT_MAX;
    
    priority_queue<int, vi, greater<int>> mnh;
    
    for(int i = 0; i < n; i++) {
        mnh.push(v[i]);
        if(sz(mnh) > k) mnh.pop();
    }
    
    return mnh.top();
}

