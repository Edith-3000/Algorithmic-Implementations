ll query_to_judge(int L, int R) {
	if(R < L) return 0LL;
	
	cout << "? " << (R - L + 1) << " ";
	for(ll i = L; i <= R; i++) cout << i + 1 << " ";
	cout << endl;
	
	ll sum; cin >> sum;
	return sum; 
}

// https://codeforces.com/blog/entry/114143
void solve()
{
  	ll n; cin >> n;
  	vll v(n);
  	for(ll i = 0; i < n; i++) cin >> v[i];
  	
  	vll pref(n, 0LL);
  	
  	for(ll i = 0; i < n; i++) {
  		pref[i] = v[i];
  		if(i) pref[i] += pref[i - 1];
  	}
  	
  	ll res = -1;
  	
  	ll L = 0, R = n - 1;
  	
  	while(L <= R) {
  		ll mid = L + ((R - L) >> 1LL);
  		
  		ll x = query_to_judge(L, mid);
  		
  		ll csx = pref[mid];
  		if((L - 1) >= 0) csx -= pref[L - 1];
  		
  		if(x == csx) {
  			L = mid + 1;
  		}
  		
  		else {
  			R = mid - 1;
  			res = mid;
  		}
  	}
  	
  	cout << "! " << res + 1 << endl;
}

