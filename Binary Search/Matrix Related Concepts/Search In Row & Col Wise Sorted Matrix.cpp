pii search_grid(vvi &v, int k) {
	int n = sz(v);
	if(n == 0) return {-1, -1};
	int m = sz(v[0]);
	
	int i = 0, j = m - 1;
	
	while(i < n and j >= 0) {
		if(v[i][j] == k) return {i, j};
		else if(k > v[i][j]) i++;
		else j--;
	}
	
	return {-1, -1};
}

void solve()
{
  	int n, m; cin >> n >> m;
  	vvi v(n, vi(m));
  	
  	for(int i = 0; i < n; i++) {
  		for(int j = 0; j < m; j++) cin >> v[i][j];
  	}
  	
  	int q; cin >> q;
  	
  	while(q--) {
  		int x; cin >> x;
  		pii res = search_grid(v, x);
  		cout << "(" << res.F << ", " << res.S << ")\n"; 
  	}
}

