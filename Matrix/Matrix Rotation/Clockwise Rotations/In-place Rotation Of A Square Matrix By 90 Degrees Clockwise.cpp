void rotate_clock(vvi &v) {
    int n = (int)v.size();
    if(n == 0) return;
    
    // consider all concentric squares of the matrix one by one
    for(int i = 0; i < (n / 2); i++) {
        // consider elements in group of 4 in the current square
        for(int j = i; j < (n - i - 1); j++) {
            // store current cell in tmp variable
            int tmp = v[i][j];
            
            // move value from left to top
            v[i][j] = v[n - j - 1][i];
            
            // move value from bottom to left
            v[n - j - 1][i] = v[n - i - 1][n - j - 1];
            
            // move value from right to bottom
            v[n - i - 1][n - j - 1] = v[j][n - i - 1];
            
            // move value from top to right
            v[j][n - i - 1] = tmp;
        }
    }
}

