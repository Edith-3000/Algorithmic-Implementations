// Function to find in-place transpose of a square matrix
void transpose(vvi &v) {
    int n = (int)v.size();
    if(n == 0) return;
    
    for(int i = 0; i < n; i++) {
        for(int j = 0; j < i; j++) swap(v[i][j], v[j][i]);
    }
}

void rotate_clock(vvi &v) {
	int n = (int)v.size();
	if(n == 0) return;
	
	transpose(v);
	
	// now reversing each row
	for(int i = 0; i < n; i++) {
		for(int j = 0; j < (n / 2); j++) swap(v[i][j], v[i][n-j-1]);
	}
}

