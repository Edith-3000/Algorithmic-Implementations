// NOTE: v[][] is a square matrix
vector<vpii> find_answer(vvi &v) {
	int n = sz(v);
	vector<vpii> res;
  	
  	set<pii> st;
  	
  	for(int i = 0; i < n; i++) {
  		for(int j = 0; j < n; j++) {
  			if(st.find({i, j}) != st.end()) continue;
  			
  			int i1 = j, j1 = n - 1 - i;
  			int i2 = j1, j2 = n - 1 - i1;
  			int i3 = j2, j3 = n - 1 - i2;
  			  			
  			st.insert({i, j});
  			st.insert({i1, j1});
  			st.insert({i2, j2});
  			st.insert({i3, j3});
  			
  			vpii tmp;
  			
  			tmp.pb({i, j});
  			tmp.pb({i1, j1});
  			tmp.pb({i2, j2});
  			tmp.pb({i3, j3});
  			
  			res.pb(tmp);
  		}
  	}  	
  	
  	return res;
}

void solve()
{
  	int n; cin >> n;
  	
  	vvi v(n, vi(n));
  	
  	for(int i = 0; i < n; i++) {
  		for(int j = 0; j < n; j++) {
  			cin >> v[i][j];
  		}
  	}
  	
  	vector<vpii> res = find_answer(v);
  	
  	for(auto v: res) {
  		for(auto p: v) {
  			cout << "(" << p.F << ", " << p.S << ") ";
  		}
  		
  		cout << "\n";
  	}
}

