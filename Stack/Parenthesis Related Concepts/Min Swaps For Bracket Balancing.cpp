// function to check if brackets are balanced in the string s
bool is_balanced(string &s) {
	int n = (int)s.size();
	int cnt = 0;
	
	for(int i = 0; i < n; i++) {
        if(s[i] == '[') cnt++;
        else {
            if(cnt <= 0) return 0;
            else cnt--;
        }
    }
    
    return cnt == 0;
}

int min_swaps(string &s) {
	int n = sz(s);
	if(n == 0) return 0;
	if(n & 1) return -1;
	
	// to keep track of positions of '['
	vi v;
	for(int i = 0; i < n; i++) {
		if(s[i] == '[') v.pb(i);
	}
	
	int open = 0, pos = 0;
	int res = 0;
	
	for(int i = 0; i < n; i++) {
		if(s[i] == '[') {
			open += 1;
			pos += 1;
		}
		
		else {
			if(open > 0) open -= 1;
			
			// we have encountered an unbalanced part of string
			else {
				// increment sum by number of swaps required
            	// i.e. position of next '[' - current position
				res += (v[pos] - i);
				swap(s[v[pos]], s[i]);
				pos += 1;
				open = 1;
			}
		}
	}
	
	if(is_balanced(s)) return res;
	else return -1;
}

