/******************************************************************************************************************************/

void helper(int idx, int sum, int n, vi &v, vi &res) {
	if(idx == n) {
		res.pb(sum);
		return;
	}
	
	int sum1 = sum, sum2 = sum;
	sum2 += v[idx];
	idx += 1;
	
	helper(idx, sum1, n, v, res);
	helper(idx, sum2, n, v, res);
}

vi subset_sums(vi &v) {
	int n = sz(v), sum = 0;
	vi res;
	
	helper(0, sum, n, v, res);
	
	return res;
}

