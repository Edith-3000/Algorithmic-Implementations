class TreeNode {
	public:
		int val;
		TreeNode *left;
		TreeNode *right;
		TreeNode(): val(0), left(NULL), right(NULL) {}
		TreeNode(int data): val(data), left(NULL), right(NULL) {}
		TreeNode(int data, TreeNode *left, TreeNode *right): val(data), left(left), right(right) {}
};

// Function to return the zigzag level order traversal of a given binary tree
vvi zig_zag(TreeNode *root) {
	vvi res;
	if(root == NULL) return res;
	
	// create an empty deque and enqueue the root node
	deque<TreeNode*> dq;
	dq.push_back(root);
	
	bool flag = 1;
	
	// loop till deque is empty
	while(!dq.empty()) {
		// calculate the total number of nodes at the current level
		int sz = dq.size();
		
		// to store the current level
		vi level;
				
		for(int i = 0; i < sz; i++) {
			if(flag == 1) {
				TreeNode *cur = dq.front();
				dq.pop_front();
				level.pb(cur->val);
				
				if(cur->left) dq.push_back(cur->left);
				if(cur->right) dq.push_back(cur->right);
			}
			
			else {
				TreeNode *cur = dq.back();
				dq.pop_back();
				level.pb(cur->val);
				
				if(cur->right) dq.push_front(cur->right);
				if(cur->left) dq.push_front(cur->left);
			}
		}
			
		res.pb(level);
		
		flag = !flag;
	}
	
	return res;
}

