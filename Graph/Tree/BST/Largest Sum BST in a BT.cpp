struct info {
    // sum of nodes in the subtree rooted at the current root
    int sum; 
    
    // minimum value in the subtree rooted at the current root, if subtree rooted at current root is a BST, else it will contain INT_MIN
    int mn;
    
    // maximum value in the subtree rooted at the current root, if subtree rooted at current root is a BST, else it will contain INT_MAX
    int mx; 
    
    info(int sum, int mn, int mx) {
        this->sum = sum;
        this->mn = mn;
        this->mx = mx;
    }
};

info largest_bst(TreeNode *root, int &res) {
    // base case
    if(root == NULL) return {0, INT_MAX, INT_MIN};
    
    info lf = largest_bst(root->left, res);
    info rg = largest_bst(root->right, res);
    
    res = max(res, lf.sum);
    res = max(res, rg.sum);

    if((lf.mx < root->val) and (root->val < rg.mn)) {
        res = max(res, lf.sum + root->val + rg.sum);
        return {lf.sum + root->val + rg.sum, min(lf.mn, root->val), max(rg.mx, root->val)};
    }
    
    else return {INT_MIN, INT_MIN, INT_MAX};
}

