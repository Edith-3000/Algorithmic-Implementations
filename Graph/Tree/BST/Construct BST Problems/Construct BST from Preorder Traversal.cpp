// function to construct a BST from elements in pre[idx, n - 1] such that
// the root node of that BST can't be >= the upper bound given
// NOTE: idx must be passed by reference
TreeNode* helper(vi &pre, int &idx, int bound) {
	// base case
	if((idx >= sz(pre)) or (pre[idx] >= bound)) return NULL;
	
	TreeNode *root = new TreeNode(pre[idx]);
	idx += 1;
	
	root->left = helper(pre, idx, root->val);
	root->right = helper(pre, idx, bound);
	
	return root;
}

TreeNode* build_tree(vi &pre) {
	int n = sz(pre);
	
	int bound = INT_MAX;
	int idx = 0;
	
	TreeNode* root = helper(pre, idx, bound);
	
	return root;
}

