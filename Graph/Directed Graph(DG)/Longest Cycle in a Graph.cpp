// to store the input graph
vvi g;

// n = #vertices
int n;

// to check if node's label is ACTIVE_NODE or EXPLORED_NODE
vi vis;

// to store the depth f node 'i', in the current DFS tree exploration
vi depth;

// to label a node which is present in the current internal call stack
int ACTIVE_NODE = 1;

// to label a node which was explored in some previous iteration
int EXPLORED_NODE = 2;

void dfs(int cur, int dep, int &res) {
	if(vis[cur] == ACTIVE_NODE) {
		res = max(res, dep - depth[cur]);
		return;
	}
	
	if(vis[cur] == EXPLORED_NODE) return;
	
	vis[cur] = ACTIVE_NODE;
	depth[cur] = dep;
	
	for(auto x: g[cur]) {
		dfs(x, dep + 1, res);
	}
	
	vis[cur] = EXPLORED_NODE;
}

int longest_cycle_length(vi &edges) {
	g.clear();
	g.resize(n);
	
	vis.clear();
	vis.resize(n, 0);
	
	depth.clear();
	depth.resize(n, 0);
	
	for(int i = 0; i < n; i++) {
		if(edges[i] == -1) continue;
		g[i].pb(edges[i]);
	}
	
	int res = -1;
	
	for(int i = 0; i < n; i++) {
		if(vis[i] == EXPLORED_NODE) continue;
		dfs(i, 0, res);
	}
	
	return res;
}

void solve()
{
  	cin >> n;
  	vi edges(n);
  	for(int i = 0; i < n; i++) cin >> edges[i];
  	
  	int res = longest_cycle_length(edges);
  	
  	cout << res << "\n";
}

