// stores the input graph
vector<vpii> g;

// n = #nodes, m = #edges
int n, m;

struct node {
	int id, depth, cost;
};

int find_least_cost(int src, int dest, int edges_limit) {
	queue<node> q;
	
	q.push({src, 0, 0});
	
	int res = INT_MAX;
	
	while(!q.empty()) {
		node nd = q.front();
		q.pop();
		
		int cur = nd.id;
        int dpt = nd.depth;
        int cst = nd.cost;
        
        if(cur == dest) {
        	res = min(res, cst);
        }
        
        if(dpt > edges_limit) {
        	break;
        }
        
        for(auto x: g[cur]) {
        	q.push({x.F, dpt + 1, cst + x.S});
        }
	}
		
	return res;
}

void solve()
{
  	cin >> n >> m;
    
    g.clear(); g.resize(n);
    
    // 0-based vertices
    for(int i = 0; i < m; i++) {
        int x, y, wt; cin >> x >> y >> wt;
        g[x].pb({y, wt});
    }
    
    int edges_limit; cin >> edges_limit;
    int src, dest; cin >> src >> dest;
    
    int res = find_least_cost(src, dest, edges_limit);
    cout << res << "\n";
}

