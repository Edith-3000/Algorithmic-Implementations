string Kth_permutation(int n, int k) {
	int fact = 1;
	vi v;

	for(int i = 1; i < n; i++) {
		fact *= i;
		v.pb(i);
	}
	
	v.pb(n);
	k -= 1;

	string res = "";
	
	while(true) {
		res = res + to_string(v[k / fact]);
		v.erase(v.begin() + (k / fact));
		if(sz(v) == 0) break;
		k %= fact;
		fact /= sz(v);
	}
	
	return res;
}

