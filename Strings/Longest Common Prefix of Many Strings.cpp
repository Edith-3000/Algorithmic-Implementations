string find_ans(vector<string> &v) {
	string res = "";
	int n = v.size();
	if(n == 0) return res;
	if(n == 1) return v[0];
	
	sort(v.begin(), v.end());	
	
	int mn = min(v[0].size(), v[n-1].size());
	int i = 0;
	
	while(i < mn and v[0][i] == v[n-1][i]) {
		res += v[0][i];
		i += 1;
	}
	
	return res;
}

void solve()
{
  	int n; cin >> n;
  	vector<string> v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	cout << find_ans(v);
}

