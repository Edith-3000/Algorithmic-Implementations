string longest_common_prefix(vs &v) {
    int n = sz(v);
    
    string res = v[0];
    
    for(int i = 1; i < n; i++) {
        int k = 0;
        
        for(; k < min(res.size(), v[i].size()); k++) {
            if(res[k] != v[i][k]) break;
        }
        
        res = res.substr(0, k);
    }
    
    return res;
}

void solve()
{
  	int n; cin >> n;
  	vs v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	cout << longest_common_prefix(v);
}

