// Function to check if 's' occurs as a subsequence in 't'
bool is_subsequence(string &s, string &t) {
	int n = sz(s), m = sz(t);
	
	int idx = 0;
	
	for(int i = 0; i < m; i++) {
		if(idx == n) return 1;
		if(s[idx] == t[i]) idx += 1;
	}
	
	return (idx == n);
}

