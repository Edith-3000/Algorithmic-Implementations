// Function to check if 's' occurs as a subsequence in 't'
bool is_subsequence(string &s, string &t) {
	int n = sz(s), m = sz(t);
	
	// assuming that both strings consists of lowercase latin letters
	vvi positions(26);
	
	for(int j = 0; j < m; j++) {
		positions[t[j] - 'a'].pb(j);
	}
	
	int prev = -1;
	
	for(int i = 0; i < n; i++) {
		auto it = upper_bound(positions[s[i] - 'a'].begin(), positions[s[i] - 'a'].end(), prev);
		if(it == positions[s[i] - 'a'].end()) return 0;
		prev = *it;
	}
	
	return 1;
}

