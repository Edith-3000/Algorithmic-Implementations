// Function to count the #distinct subsequences of string s which equals string t.
int cnt_ways(string &s, string &t, int n, int m) {
	vi prev_row(m + 1), curr_row(m + 1);

	prev_row[0] = 1; curr_row[0] = 1;

	for(int j = 1; j <= m; j++) prev_row[j] = 0;
		
	for(int i = 1; i <= n; i++) { 
		for(int j = 1; j <= m; j++) {
			if(s[i - 1] == t[j - 1]) curr_row[j] = prev_row[j] + prev_row[j - 1];
			else curr_row[j] = prev_row[j];
		}
		
		prev_row = curr_row;
	}
	
	return prev_row[m];
}

