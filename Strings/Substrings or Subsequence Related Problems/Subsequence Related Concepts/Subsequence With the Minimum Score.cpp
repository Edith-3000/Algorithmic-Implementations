// Function to check if string 't' is a subsequence of string 's'
bool is_subsequence(string &s, string &t) {
	int n = sz(s), m = sz(t);
	
	// base case(s)
	if(n < m) return 0;
	
	int idx = 0;
	
	for(int i = 0; i < n; i++) {
		if(idx == m) return 1;
		if(s[i] == t[idx]) idx += 1;
	}
	
	return (idx == m);
}

int minimum_score(string &s, string &t) {
	int n = sz(s), m = sz(t);
	
	if(is_subsequence(s, t)) return 0;
	
	// to store the result
	int res = m;
	
	for(int l = 0; l < m; l++) {
		for(int r = l; r < m; r++) {
			string str = t.substr(0, l);
			if(r != m) str += t.substr(r + 1, m - r - 1);
			
			if(is_subsequence(s, str)) {
				res = min(res, r - l + 1);
			}
		}
	}
	
	return res;
}

void solve()
{
  	string s, t; cin >> s >> t;
  	
  	int res = minimum_score(s, t);
  	
  	cout << res << "\n";
}

