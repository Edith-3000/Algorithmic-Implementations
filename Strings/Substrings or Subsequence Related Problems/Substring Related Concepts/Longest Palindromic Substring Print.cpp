// NOTE: this function considers a string of length = 1, as palindromic
string print_LPSS(string &s) {
	int n = sz(s);
	if(n == 0) return "";
	
	int len = 0, start;
	
	for(int c = 0; c < n; c++) {
		// for odd length substrings
		int i = c - 1, j = c + 1;
		while(i >= 0 and j < n and s[i] == s[j]) {
			if(j - i + 1 > len) {
				len = j - i + 1;
				start = i;
			}
			
			i--, j++;
		}
		
		// for even length substrings
		i = c - 1, j = c;
		while(i >= 0 and j < n and s[i] == s[j]) {
			if(j - i + 1 > len) {
				len = j - i + 1;
				start = i;
			}
			
			i--, j++;
		}
	}
	
	string res = "";
		
	if(len == 0) res.pb(s[0]);
	else res = s.substr(start, len);
	
	return res;
}

