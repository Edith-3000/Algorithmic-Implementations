// dp[i][j] = store that whether the substring s[i...j] is palindromic
//            or not
vvb dp;

// NOTE: this function considers a string of length = 1, as palindromic
string print_LPSS(string &s) {
	int n = sz(s);
	
	dp.clear();
	dp.resize(n, vb(n));
	
	int len = 0, start;
	
	for(int g = 0; g < n; g++) { 
		for(int i = 0, j = g; j < n; i++, j++) {
			if(g == 0) dp[i][j] = 1;
			
			else if(g == 1) {
				if(s[i] == s[j]) dp[i][j] = 1;
				else dp[i][j] = 0;	
			}
			
			else {
				if(s[i] == s[j] and dp[i+1][j-1]) dp[i][j] = 1;
				else dp[i][j] = 0;
			}
			
			if(dp[i][j] and (g + 1) > len) {
				len = g + 1;
				start = i;
			}
		} 
	}
	
	string res = s.substr(start, len);

	return res;
}

