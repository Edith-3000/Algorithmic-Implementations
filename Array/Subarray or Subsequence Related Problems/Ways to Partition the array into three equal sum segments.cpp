int find_ans(vi &v) {
	int n = v.size();
	
	ll sum = 0LL;
	for(int i = 0; i < n; i++) sum += v[i];
	
	if(sum % 3) return 0;
	int res = 0, num1 = sum/3, num2 = 2 * (sum / 3), cnt1 = 0;
	sum = 0LL;
	
	for(int i = 0; i < n - 1; i++) {
		sum += v[i];
		if(sum == num2) res += cnt1;
		if(sum == num1) cnt1 += 1;
	}
	
	return res;
}

