void solve(int n)
{
  	vi v(n);
  	for(int i = 0; i < n; i++) cin >> v[i];
  	
  	vpii buy, sell;
  	for(int i = 0; i < n; i++) {
  		if(v[i] > 0) buy.pb({v[i], i});
  		else if(v[i] < 0) sell.pb({v[i], i});
  	}
  	
  	ll res = 0LL;
  	int i = 0, j = 0;
  	
  	while(i < sz(buy) and j < sz(sell)) {
  		int settlement = min(buy[i].F, -sell[j].F);
  		res += ((ll)settlement * abs(buy[i].S - sell[j].S));
  		buy[i].F -= settlement;
  		sell[j].F += settlement;
  		if(buy[i].F == 0) i++;
  		if(sell[j].F == 0) j++;
  	}
  	
  	cout << res << "\n";
}

