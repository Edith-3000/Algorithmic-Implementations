// Returns minimum number of platforms required so that no train waits
int min_platforms(vi &arr, vi &dep, int n) {
    // sort arrival and departure arrays
    sort(arr.begin(), arr.end());
    sort(dep.begin(), dep.end());
 
    // plat_needed indicates number of platforms needed at any instant of time
    int plat_needed = 1, res = 1;
    int i = 1, j = 0;
 
    // similar to merge in merge sort to process all events in sorted order
    while (i < n && j < n) {
        // if the arrival train arrives on or before the departure train, 
        // increment count of platforms needed
        if (arr[i] <= dep[j]) {
            plat_needed++;
            i++;
        }
 
        // else if the departure train leaves before the arrival train,
        // decrement count of platforms needed
        else if (arr[i] > dep[j]) {
            plat_needed--;
            j++;
        }
 
        // update result if needed
        if (plat_needed > res) res = plat_needed;
    }
 
    return res;
}

void solve()
{
    int n; cin >> n;
    vi arr(n), dep(n);
    
    for(int i = 0; i < n; i++) cin >> arr[i];
    for(int i = 0; i < n; i++) cin >> dep[i];
    
    cout << min_platforms(arr, dep, n);
}

